func TestPRK_DeriveKey_PanicsOnLargeKeyRequest(t *testing.T) {
	defer func() {
		if r := recover(); r == nil {
			t.Errorf("unexpected success")
		}
	}()

	hk := New(sha256.New, nil, nil)
	hk.DeriveKey(8161, nil)
}

func Test_roundUp(t *testing.T) {
	testCases := []struct {
		n, m     int
		expected int
	}{
		{n: 0, m: 3, expected: 0},
		{n: 1, m: 3, expected: 3},
		{n: 2, m: 3, expected: 3},
		{n: 3, m: 3, expected: 3},
		{n: 4, m: 3, expected: 6},
		{n: 5, m: 3, expected: 6},
		{n: 6, m: 3, expected: 6},
	}
	for _, testCase := range testCases {
		actual := roundUp(testCase.n, testCase.m)
		if actual != testCase.expected {
			t.Errorf("unexpected result: got %d, want %d", actual, testCase.expected)
		}
	}
}
